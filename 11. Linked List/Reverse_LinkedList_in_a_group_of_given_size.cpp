#include<bits/stdc++.h>
using namespace std;


struct node
{
    int data;
    struct node* next;
    
    node(int x){
        data = x;
        next = NULL;
    }
    
};

/* Function to print linked list */
void printList(struct node *node)
{
    while (node != NULL)
    {
        printf("%d ", node->data);
        node = node->next;
    }
    printf("\n");
}

class Solution
{
    public:
    struct node *reverse (struct node *head, int k)
    { 
        struct node *pre=NULL,*curr=head,*next;
        
            int c=k;
            while(curr!=NULL && c){
                next=curr->next;
                curr->next=pre;
                pre=curr;
                curr=next;
                c--;
            }
            if(head!=NULL){
                head->next=reverse(next,k);
            }
            return pre;
    }
};

